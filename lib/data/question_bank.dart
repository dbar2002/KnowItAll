  static List<String> get categories =>
      allQuestions.map((q) => q.category).toSet().toList()..sort();

  static List<Question> getByCategory(String category) =>
      allQuestions.where((q) => q.category == category).toList();

  static List<Question> getByDifficulty(String difficulty) =>
      allQuestions.where((q) => q.difficulty == difficulty).toList();

  static List<Question> getRandomQuiz({int count = 10, String? category}) {
    List<Question> pool =
        category != null ? getByCategory(category) : List.from(allQuestions);
    pool.shuffle();
    return pool.take(count).toList();
  }
